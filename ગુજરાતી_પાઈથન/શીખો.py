def શીખવાનું_શરૂ_કરો():
    """ઈન્ટરએક્ટિવ લર્નિંગ મોડ શરૂ કરે છે"""
    
    if console:
        console.clear()
        console.print(Panel.fit("[bold magenta]ગુજરાતી પાઈથન શીખો[/bold magenta]", border_style="magenta"))
        console.print("આવો પાઈથન શીખીએ! બહાર નીકળવા માટે 'exit' લખો.\n")
    else:
        print("=== ગુજરાતી પાઈથન શીખો ===\n")

    for i, પાઠ in enumerate(પાઠો):
        # પાઠ બતાવો
        if console:
            console.print(Panel(Markdown(પાઠ.વર્ણન), title=f"પાઠ {i+1}: {પાઠ.શીર્ષક}", border_style="cyan"))
            console.print(Panel(પાઠ.ઉદાહરણ, title="ઉદાહરણ", border_style="green"))
            console.print(f"\n[bold yellow]ચેલેન્જ:[/bold yellow] {પાઠ.ટાસ્ક}\n")
        else:
            print(f"\n--- પાઠ {i+1}: {પાઠ.શીર્ષક} ---")
            print(પાઠ.વર્ણન)
            print(f"ઉદાહરણ:\n{પાઠ.ઉદાહરણ}")
            print(f"\nચેલેન્જ: {પાઠ.ટાસ્ક}\n")
        
        # લૂપ જ્યાં સુધી યુઝર સાચો જવાબ ન આપે
        while True:
            try:
                if console:
                    કોડ = console.input("[bold blue]તમારો કોડ >>> [/bold blue]")
                else:
                    કોડ = input("તમારો કોડ >>> ")
                
                if કોડ.lower() in ['exit', 'quit', 'બહાર']:
                    print("આવજો!")
                    return

                # કોડ ચલાવો
                પરિણામ = ગુજરાતી_કોડ_ચલાવો(કોડ)
                
                # આઉટપુટ બતાવો
                if પરિણામ['આઉટપુટ']:
                    if console:
                        console.print(Panel(પરિણામ['આઉટપુટ'], title="તમારું આઉટપુટ"))
                    else:
                        print(f"આઉટપુટ:\n{પરિણામ['આઉટપુટ']}")
                
                if પરિણામ['એરર']:
                    if console:
                        console.print(f"[bold red]ભૂલ:[/bold red] {પરિણામ['એરર']}")
                    else:
                        print(f"ભૂલ: {પરિણામ['એરર']}")
                    continue

                # ચકાસણી
                if પાઠ.ઉકેલ_ચકાસણી(કોડ, પરિણામ):
                    if console:
                        console.print("\n[bold green]શાબાશ! જવાબ સાચો છે! 🎉[/bold green]\n")
                        time.sleep(1)
                    else:
                        print("\nશાબાશ! જવાબ સાચો છે! 🎉\n")
                    break
                else:
                    if console:
                        console.print("[bold red]ખોટો જવાબ. ફરી પ્રયત્ન કરો.[/bold red]")
                    else:
                        print("ખોટો જવાબ. ફરી પ્રયત્ન કરો.")
            
            except KeyboardInterrupt:
                print("\nબહાર નીકળી રહ્યા છીએ...")
                return

    if console:
        console.print(Panel("[bold green]અભિનંદન! તમે બધા પાઠ પૂરા કર્યા![/bold green]", border_style="green"))
    else:
        print("\nઅભિનંદન! તમે બધા પાઠ પૂરા કર્યા!")
