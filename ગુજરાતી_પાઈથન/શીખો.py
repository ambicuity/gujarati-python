def પાઠ_1_ચકાસણી(કોડ: str, પરિણામ: Dict[str, Any]) -> bool:
    """પાઠ 1: છાપો - ચકાસણી"""
    if "જય હિન્દ" in પરિણામ['આઉટપુટ']:
        return True
    return False

def પાઠ_2_ચકાસણી(કોડ: str, પરિણામ: Dict[str, Any]) -> bool:
    """પાઠ 2: ચલ - ચકાસણી"""
